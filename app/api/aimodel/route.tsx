export async function POST(req: NextRequest) {
  try {
    const { messages, isFinal } = await req.json();

    const completion = await openai.chat.completions.create({
      model: "openai/gpt-4o-mini",
      response_format: {
        type: "json_object",
      },
      messages: [
        {
          role: "system",
          content: isFinal ? FINAL_PROMPT : PROMPT,
        },
        ...messages,
      ],
    });

    const rawContent: unknown = completion?.choices?.[0]?.message?.content;

    if (!rawContent) {
      return NextResponse.json(
        {
          error: "No response generated by AI.",
        },
        {
          status: 500,
        }
      );
    }

    const normalizeUi = (value: unknown): UiStage | null => {
      if (typeof value !== "string") return null;
      const normalized = value.trim().toLowerCase().replace(/\s+/g, "");
      if (normalized === "groupsize") return "groupsize";
      if (normalized === "budget") return "budget";
      if (normalized === "tripduration") return "tripduration";
      if (normalized === "final") return "final";
      if (normalized === "") return "";
      return null;
    };

    // Fallback ONLY used if the model fails to return a valid "ui" value at all.
    // Checked in actual conversation order (not an arbitrary priority) and only
    // considers whichever stage hasn't been used yet, so it can't jump ahead
    // or re-trigger an earlier widget.
    const inferUiFromText = (text: string, nextExpected: UiStage) => {
      const lower = text.toLowerCase();
      if (/final|complete trip plan|here is your (trip )?plan|your trip plan is ready/.test(lower)) {
        return "final" as UiStage;
      }
      if (nextExpected === "groupsize" && /group size|solo|couple|family|friends/.test(lower)) {
        return "groupsize" as UiStage;
      }
      if (nextExpected === "budget" && /\bbudget\b/.test(lower)) {
        return "budget" as UiStage;
      }
      if (nextExpected === "tripduration" && /trip duration|how many days/.test(lower)) {
        return "tripduration" as UiStage;
      }
      return "" as UiStage;
    };

    let parsed: any = null;

    try {
      if (typeof rawContent === "string") {
        try {
          parsed = JSON.parse(rawContent);
        } catch (e) {
          parsed = { resp: rawContent };
        }
      } else if (Array.isArray(rawContent)) {
        const joined = (rawContent as any[])
          .map((c: any) => (typeof c === "string" ? c : c?.text ?? JSON.stringify(c)))
          .join("");
        try {
          parsed = JSON.parse(joined);
        } catch (e) {
          parsed = { resp: joined };
        }
      } else if (typeof rawContent === "object" && rawContent !== null) {
        const objContent = rawContent as Record<string, unknown>;
        if (typeof objContent.text === "string") {
          try {
            parsed = JSON.parse(objContent.text);
          } catch (e) {
            parsed = { resp: objContent.text, ui: objContent.ui ?? null };
          }
        } else {
          parsed = objContent;
        }
      } else {
        parsed = { resp: String(rawContent) };
      }
    } catch (e) {
      parsed = { resp: String(rawContent), error: "Failed to parse AI response", details: String(e) };
    }

    const respText = typeof parsed?.resp === "string" ? parsed.resp : String(parsed?.resp ?? "");

    // --- Deterministic ordering guard ---
    // Look at the ui field on every past assistant message already in the
    // conversation to figure out which of the 3 ordered widget stages have
    // already been shown, so we know what's allowed to come next.
    const alreadyUsedStages = new Set(
      (Array.isArray(messages) ? messages : [])
        .filter((m: any) => m?.role === "assistant" && typeof m?.ui === "string")
        .map((m: any) => m.ui.trim().toLowerCase().replace(/\s+/g, ""))
        .filter((ui: string) => (UI_SEQUENCE as readonly string[]).includes(ui))
    );
    const nextExpectedStage: UiStage =
      UI_SEQUENCE.find((stage) => !alreadyUsedStages.has(stage)) ?? "final";

    let uiValue = normalizeUi(parsed?.ui ?? parsed?.UI ?? "");
    if (uiValue === null) {
      // Model returned something unrecognized/missing — fall back to
      // conservative text inference restricted to the next expected stage.
      uiValue = inferUiFromText(respText, nextExpectedStage);
    } else if (
      (UI_SEQUENCE as readonly string[]).includes(uiValue) &&
      uiValue !== nextExpectedStage
    ) {
      // Model tried to show an out-of-order or repeated widget
      // (e.g. asked for "budget" again after it was already collected,
      // or jumped straight to "tripduration" before "budget").
      // Correct it to the widget that's actually next in sequence.
      uiValue = nextExpectedStage;
    }

    return NextResponse.json({ resp: respText, ui: uiValue, trip_plan: parsed?.trip_plan ?? null });
  } catch (error) {
    console.error("AI Route Error:", error);

    return NextResponse.json(
      {
        error: "Internal Server Error",
        details: error instanceof Error ? error.message : "Unknown error",
      },
      {
        status: 500,
      }
    );
  }
}